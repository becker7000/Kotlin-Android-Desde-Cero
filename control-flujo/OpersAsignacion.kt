
fun main() {

    var puntos = 20

    println("\n\t Puntos iniciales: $puntos")

    puntos += 5  // suma 5
    println("\n\t Después de += 5: $puntos")

    puntos -= 3  // resta 3
    println("\n\t Después de -= 3: $puntos")

    puntos *= 2  // multiplica por 2
    println("\n\t Después de *= 2: $puntos")

    puntos /= 4  // divide por 4
    println("\n\t Después de /= 4: $puntos")

    puntos %= 3  // obtiene el módulo
    println("\n\t Después de %= 3: $puntos")

}
